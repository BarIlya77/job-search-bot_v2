async def handle_main_menu(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ –Ω–∞–∂–∞—Ç–∏–π –Ω–∞ –∫–Ω–æ–ø–∫–∏ –≥–ª–∞–≤–Ω–æ–≥–æ –º–µ–Ω—é"""
    text = update.message.text

    if text == "üîç –ü–æ–∏—Å–∫ –≤–∞–∫–∞–Ω—Å–∏–π":
        await update.message.reply_text("–§—É–Ω–∫—Ü–∏—è –ø–æ–∏—Å–∫–∞ –≤ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ...")
    elif text == "‚öôÔ∏è –§–∏–ª—å—Ç—Ä—ã":
        await update.message.reply_text("–ù–∞—Å—Ç—Ä–æ–π–∫–∞ —Ñ–∏–ª—å—Ç—Ä–æ–≤ –≤ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ...")
    elif text == "üìä –°—Ç–∞—Ç—É—Å":
        await update.message.reply_text("–°—Ç–∞—Ç—É—Å –≤ —Ä–∞–∑—Ä–∞–±–æ—Ç–∫–µ...")
    elif text == "‚ùì –ü–æ–º–æ—â—å":
        await help_command(update, context)
    else:
        await update.message.reply_text(
            "–ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–Ω–æ–ø–∫–∏ –º–µ–Ω—é –∏–ª–∏ –∫–æ–º–∞–Ω–¥—ã /help",
            reply_markup=get_main_keyboard()
        )


